package cn.opt.po;

public class Collect {
    private Integer colId;

    private Integer stuId;

    private Integer vioId;

    private Integer kninfoId;

    private Integer brushId;

    private Integer flag;

    public Integer getColId() {
        return colId;
    }

    public void setColId(Integer colId) {
        this.colId = colId;
    }

    public Integer getStuId() {
        return stuId;
    }

    public void setStuId(Integer stuId) {
        this.stuId = stuId;
    }

    public Integer getVioId() {
        return vioId;
    }

    public void setVioId(Integer vioId) {
        this.vioId = vioId;
    }

    public Integer getKninfoId() {
        return kninfoId;
    }

    public void setKninfoId(Integer kninfoId) {
        this.kninfoId = kninfoId;
    }

    public Integer getBrushId() {
        return brushId;
    }

    public void setBrushId(Integer brushId) {
        this.brushId = brushId;
    }

    public Integer getFlag() {
        return flag;
    }

    public void setFlag(Integer flag) {
        this.flag = flag;
    }
}